// fileTemplateProxy is the master template for proxy mode. It emits a
// *.mcp.go file whose handlers build *http.Request objects directly,
// dispatch through cfg.HTTPClient, and apply env-var-driven authentication
// — no oapi-codegen client needed. The companion go.mod + main.go are
// emitted separately by pkg/generator/scaffold.go.
